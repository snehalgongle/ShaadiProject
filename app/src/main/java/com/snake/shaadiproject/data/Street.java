package com.snake.shaadiproject.data;
public class Street
{
    private long number;

    private String name;

    public void setNumber(int number){
        this.number = number;
    }

    public long getNumber() {
        return number;
    }

    public void setNumber(long number) {
        this.number = number;
    }

    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }
}